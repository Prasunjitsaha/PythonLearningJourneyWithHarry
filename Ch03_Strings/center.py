a="welcome to world!"
print(a.center(50))
print(a.center(50, '*'))  # Centers the string in a field of width 50, filling with '*'
print(a.center(50, '-'))    # Centers the string in a field of width 50, filling with '-'
print(a.center(50, ' '))  # Centers the string in a field of width 50, filling with spaces
print(a.center(50, '0'))        # Centers the string in a field of width 50, filling with '0'
 
